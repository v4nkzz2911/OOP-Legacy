    private void btnReadFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReadFileActionPerformed
        // TODO add your handling code here:
        ArrayList<Employee> ds = new ArrayList();
        try {
            //FileInputStream fis = new FileInputStream("nhanvien.dat");
            //InputStreamReader isr = new InputStreamReader(fis);
            BufferedReader br = new BufferedReader(new FileReader("nhanvien.dat"));
            String line = "";
            Employee t = new Employee();
            while ((line=br.readLine()) != null) {
                if (line.length() > 0) {
                    String[] result = line.split("\\$",-1);
                    System.out.println(line);
                    t.setName(result[0]);
                    t.setBirthday(result[1]);
                    t.setAddress(result[2]);
                    t.setSex(result[3]);
                    
                    t.setDepartment(result[4]);
                    t.setSalaryrate(Float.parseFloat(result[5]));
                    t.setSeniority(Float.parseFloat(result[6]));
                    t.setBasesalary(Integer.valueOf(result[7]));
                    t.setSalary(Float.parseFloat(result[8]));
                    ds.add(t);
                    model.addRow(new Object[]{t.getName(), t.getBirthday(), t.getSex(), t.getDepartment(), t.getSalaryrate(),t.getSeniority(),t.getBasesalary(),t.getSalary()});
                }
             //   line = br.readLine();
            }
            br.close();
            //isr.close();
            //fis.close();

        } catch (IOException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnReadFileActionPerformed

