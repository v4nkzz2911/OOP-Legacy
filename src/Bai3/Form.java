    private void btnWriteFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnWriteFileActionPerformed
        // TODO add your handling code here:
        try {
            FileOutputStream fos = new FileOutputStream("nhanvien.dat");
            OutputStreamWriter osw = new OutputStreamWriter(fos);
            BufferedWriter bw = new BufferedWriter(osw);
            String line = "";
            int i;
            for (i = 0; i < list.size(); i++) {
                
                line += list.get(i).getName() + "$";
                line += list.get(i).getBirthday() + "$";
                line += list.get(i).getAddress() + "$";
                line += list.get(i).getSex() + "$";
                line += list.get(i).getDepartment() + "$";
                line += list.get(i).getSalaryrate() + "$";
                line += list.get(i).getSeniority() + "$";
                line += list.get(i).getBasesalary() + "$";
                line += list.get(i).getSalary();
                bw.write(line);
                bw.newLine();
            }
            
            bw.close();
            fos.close();
            osw.close();
            JOptionPane.showMessageDialog(rootPane, "Lưu thành công danh sách Nhân viên");
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnWriteFileActionPerformed

